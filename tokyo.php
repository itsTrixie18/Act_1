<div class="pt-4 pb-2">
<p>Tokyo means "Eastern Capital" in Japanese, a name it was given in 1868 when the imperial capital was moved to the city of Edo. </p>
<p>The name consists of two kanji characters: "to" (東), which means east, and "kyo" (京), which means capital. </p>
